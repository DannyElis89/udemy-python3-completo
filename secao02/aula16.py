"""
Operadores aritméticos em Python
+ adição
- subtração
* multiplicação
** exponenciação
/ divisão
// divisão inteira
% módulo (resto da divisão)
() altera a ordem de precedência
"""

print('25 + 5 =', 25 + 5)
print('25 - 5 =', 25 - 5)
print('25 * 5 =', 25 * 5)
print('25 ** 5 =', 25 ** 5)
print('25 / 6 =', 25 / 6)
print('25 // 6 =', 25 // 6)
print('25 % 6 =', 25 % 6)
print('10 + 2 * 5 / 15 =', 10 + 2 * 5 / 15)
print('(10 + 2) * (5 / 15) =', (10 + 2) * (5 / 15))
